
public class WeatherForecast {
	private String skies = "";
	private int high = 0;
	private int low = 0;
	public String getSkies() {
		return skies;
	}
	public void setSkies(String skies) {
		this.skies = skies;
	}
	public int getHigh() {
		return high;
	}
	public void setHigh(int high) {
		this.high = high;
	}
	public int getLow() {
		return low;
	}
	public void setLow(int low) {
		this.low = low;
	}
	
}
